						<li><h4>Gel Moldado</h4></li>
						<p>Aplicamos um molde onde ele é um extensor, que aplicando o gel sobre este molde conseguimos alongar a suas unhas, deixando ela do tamanho desejado e também fazer a decoração que gostar. Finalizamos o alongamento com a cutilagem. <em>Tempo de atendimento: 3h</em></p>
						<li><h4>Extra Longa</h4></li>
						<p>	Aplicação extra longa. <em>Tempo de atendimento: 4h</em>	</p>
					</ul>
				<li><strong><h3>Esmaltação</h3></strong></li>
					<p>Esmaltação de longa duração, procedimento exclusivo equipe 
					<strong>Karina Vieira Nails</strong>; essa técnica pode garantir até 
					20 dias de esmalte intacta.</p>
					<ul type='square'>
						<li><h4>Banho em Gel</h4></li>
							<p>Banho em gel, é uma proteção sobre a sua unha natural feita com o gel, proporcionando mais resistência a ela, impossibilitando ela de quebrar ou lascar como acontece com a unha natural. Conseguimos fazer a decoração ou uma esmaltacção em gel, a manutenção é feita com no máximo 30 dias. Finalizamos o procedimento com a cutilagem. Tempo de atendimento: 2h	</p>
						<li><h4>Esmaltação em Gel</h4></li>
							<p>Esmaltação em gel, é uma técnica realizada sobre a unha natural da cliente, fazemos a cutilagem uma preparação para receber esse esmalte em gel que vai proporcionar durabilidade de 15/20 dias e um brilho intenso sem descascar. <em>Tempo de atendimento: 1h e 30 min</em></p>				
					</ul>
				<li><h3><strong>Manutenção</strong></h3></li>
					<p>É um processo essencial para você manter suas unhas 
					com alongamento sempre lindas e saudáveis. A manutenção deve
					ser feita sempre com um prazo de 30 dias</p>
